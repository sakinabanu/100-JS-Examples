// JavaScript Program to Sort Words in Alphabetical Order :

const a = "sakinabanu";

const b = `${a.split("").sort().join("")}`;

console.log(b); // aaabiknnsu


const c = ["kiwi","lime","Banana", "Orange", "Apple", "Mango"];
const d = c.sort();
console.log(d); // [ 'Apple', 'Banana', 'Mango', 'Orange', 'kiwi', 'lime' ]
